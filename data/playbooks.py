# Standalone reference file — shows intended playbook structure.
# In production, these steps are served via the JS playbooks dict in preview.html.

playbooks = {
    "Ransomware signature detected": [
        "Immediately isolate the affected endpoint from the network",
        "Identify the ransomware variant using AV or threat intel feeds",
        "Preserve forensic evidence — capture memory dump and disk image",
        "Notify security lead and initiate incident escalation",
        "Determine blast radius — identify other potentially infected hosts",
        "Restore affected systems from clean, verified backups",
        "Patch the initial attack vector before reconnecting to network",
        "Conduct post-incident review and update playbook",
    ],
    "Unusual data exfiltration": [
        "Identify the user account and endpoint involved",
        "Block outbound traffic from the source IP immediately",
        "Determine what data was accessed and volume transferred",
        "Preserve logs from DLP, proxy, and endpoint telemetry",
        "Suspend the user account pending investigation",
        "Notify legal, compliance, and data protection officer",
        "Assess whether breach notification obligations apply",
        "Review and tighten DLP policies post-incident",
    ],
    "Phishing email detected": [
        "Identify all recipients of the phishing email",
        "Pull and block the sender domain and IP at the email gateway",
        "Check if any recipient clicked links or opened attachments",
        "Reset credentials for any accounts that interacted with the email",
        "Scan endpoints of affected users for malware indicators",
        "Review mailbox activity for forwarding rules or suspicious logins",
        "Send user awareness notification across the organisation",
    ],
    "Brute force login attempt": [
        "Identify the targeted account and source IP address",
        "Lock the account if the login threshold has been exceeded",
        "Block the source IP at the firewall or WAF",
        "Review authentication logs for any successful logins from that IP",
        "Enable or enforce MFA on the targeted account",
        "Check for lateral movement if account was compromised",
        "Tune IDS/IPS rules to alert on similar patterns going forward",
    ],
    "DDoS traffic spike": [
        "Confirm the spike is malicious and not a legitimate traffic surge",
        "Enable rate limiting at the edge or CDN layer",
        "Contact upstream ISP or DDoS mitigation provider for scrubbing",
        "Identify source IPs or ASNs and apply null routing where possible",
        "Monitor service availability and escalate if SLAs are breached",
        "Capture traffic samples for post-incident analysis",
        "Review and update DDoS response runbooks",
    ],
    "Malware signature detected": [
        "Isolate the affected host from the network immediately",
        "Run a full antivirus and EDR scan on the endpoint",
        "Collect forensic artefacts — process list, network connections, prefetch files",
        "Identify the malware family and check for known indicators of compromise",
        "Check for persistence mechanisms (registry keys, scheduled tasks, startup items)",
        "Determine if the malware has spread to other hosts",
        "Restore from a known-good backup after full remediation",
    ],
    "Unauthorized access attempt": [
        "Review access logs to determine what resources were targeted",
        "Verify if the attempt succeeded or was blocked",
        "Identify the source IP and correlate with threat intelligence",
        "Check for other attempts from the same IP or user agent",
        "Strengthen access controls or MFA on targeted resources",
        "Document findings and close the incident with a timeline",
    ],
}